    public BiosFont(GraphicsDevice device)
    {
        var chars = Glyphs.Keys.ToArray();
        _columnByChar = new Dictionary<char, int>();
        _atlas = new Texture2D(device, GlyphSize * chars.Length, GlyphSize);

        var pixels = new Color[_atlas.Width * _atlas.Height];
        for (var c = 0; c < chars.Length; c++)
        {
            _columnByChar[chars[c]] = c;
            var rows = Glyphs[chars[c]];
            for (var y = 0; y < GlyphSize; y++)
            {
                for (var x = 0; x < GlyphSize; x++)
                {
                    var an = rows[y][x] == '#';
                    pixels[(y * _atlas.Width) + (c * GlyphSize) + x] = an ? Color.White : Color.Transparent;
                }
            }
        }

        _atlas.SetData(pixels);
    }

    public void DrawText(SpriteBatch batch, string text, Vector2 position, Color color)
    {
        var pen = position;
        foreach (var zeichen in text)
        {
            if (_columnByChar.TryGetValue(char.ToUpperInvariant(zeichen), out var column))
            {
                var source = new Rectangle(column * GlyphSize, 0, GlyphSize, GlyphSize);
                batch.Draw(_atlas, pen, source, color);
            }

            pen.X += GlyphSize;
        }
    }
