    public static double HzForIndex(int sidTableIndex)
    {
        if (sidTableIndex < MinIndex || sidTableIndex > MaxIndex)
        {
            throw new ArgumentOutOfRangeException(nameof(sidTableIndex), sidTableIndex, $"Erwartet: {MinIndex:X2}-{MaxIndex:X2}");
        }

        return SidRegisterByIndex[sidTableIndex - MinIndex] * SoundRecipes.RegisterToHz;
    }

    /// <summary>Rendert die komplette Melodie (beide Stimmen gemischt) als nahtlos schleifbares PCM-Sample.</summary>
    public static short[] Render(double noteDurationSeconds = DefaultNoteDurationSeconds)
    {
        // Kurze Attack/Release-Rampe gegen Knackser zwischen den Noten; Sustain über die
        // restliche Notendauer — Gate-Verhalten ist in der Doku nicht spezifiziert.
        const double edge = 0.005;
        var envelope = new Envelope
        {
            AttackSeconds = edge,
            DecaySeconds = 0.0,
            SustainLevel = 1.0,
            HoldSeconds = Math.Max(0.0, noteDurationSeconds - (2 * edge)),
            ReleaseSeconds = edge,
        };

        var samplesPerNote = envelope.TotalSamples(SidSynth.SampleRate);
        var buffer = new short[samplesPerNote * NotePairs.Length];

        for (var note = 0; note < NotePairs.Length; note++)
        {
            var (voice2Index, voice1Index) = NotePairs[note];
            var voice2 = SidSynth.RenderTriangle(HzForIndex(voice2Index), envelope);
            var voice1 = SidSynth.RenderTriangle(HzForIndex(voice1Index), envelope);

            var offset = note * samplesPerNote;
            for (var i = 0; i < samplesPerNote; i++)
            {
                buffer[offset + i] = (short)((voice1[i] + voice2[i]) / 2);
            }
        }

        return buffer;
    }
