namespace BoulderDash.Core.Audio;

/// <summary>
/// Die Original-C64-Titelmelodie (Peter Broadribb, elmerproductions.com/sp/peterb/sounds.html):
/// 128 Noten gleicher Dauer, endlos ohne Pause wiederholt, zwei SID-Stimmen gleichzeitig. Jede
/// Note ist ein Indexpaar (Stimme 2, Stimme 1) in die SID-Register-Tabelle darunter. Notendaten
/// und Umrechnungstabelle wurden vom Nutzer aus der Doku entnommen und zur Übernahme freigegeben.
///
/// Notendauer ist in der Doku nicht angegeben ("jede Note hat die gleiche Dauer") — nach Gehör
/// kalibriert, siehe <see cref="DefaultNoteDurationSeconds"/>.
/// </summary>
public static class ThemeTune
{
    /// <summary>Nicht dokumentiert, nach Gehör kalibriert.</summary>
    public const double DefaultNoteDurationSeconds = 0.15;

    private const int MinIndex = 0x0A;
    private const int MaxIndex = 0x3A;

